package javaFundamental;

public class NonPrimitiveDataType {
    public static void main(String[] args) {
        // Non-Primitive
        String name = "Stuti";
        System.out.println(name.length());
        // non-primitive data type have their own method/function , length() is method/function of name
        // length ko humne dot(.) operator lga kr call kiya name ki length ko
        // jb bhi hume non-primitive create krne honge hum unko create krenge (new) keywotd se
    }
}
