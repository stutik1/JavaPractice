package javaFundamental;

import java.util.Scanner;

public class Array2D {
    public static void main(String[] args) {
        int[] mark = {95,96,97};

        int[][] marks = {{95, 96, 97},{98,99,90}};
        System.out.println(marks[0][2]);  // [0] it is the 1 student index and [2] marks of 1 student index
        System.out.println(marks[1][2]);  // [1] it is the 2 student index and [2] marks of 2 student index

    }
}
